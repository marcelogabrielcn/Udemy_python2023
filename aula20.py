"""
Fatiamento de Strings
 0123456789
 Olá mundo
-987654321

Fatiamento [inicio:fim:passo] [::]
Função len - retorna quantidade de itens
"""

var = 'Olá mundo'
print(var[5])
print(var[4:])
print(var[:4])
print(var[2:7:2])
print(var[-5])
print(var[-5:])
print(len(var))
print(var[::-1])

